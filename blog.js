// Функция для показа меню навигации
function showMenu() {
    var navLinks = document.getElementById("navLinks");
    navLinks.style.right = "0";
}

// Функция для скрытия меню навигации
function hideMenu() {
    var navLinks = document.getElementById("navLinks");
    navLinks.style.right = "-200px";
}

